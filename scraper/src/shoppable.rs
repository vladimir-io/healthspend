pub fn is_shoppable(code: &str) -> bool {
    shoppable_codes().contains(code)
}

/// Determines whether a CPT code should be stored during parse.
///
/// Default behavior keeps only CMS shoppable codes for edge-sized databases.
/// Set `HS_FULL_CPT_COVERAGE=1` to store all CPT codes from machine-readable files.
pub fn should_store_code(code: &str) -> bool {
    let full = std::env::var("HS_FULL_CPT_COVERAGE")
        .ok()
        .map(|v| {
            let n = v.to_ascii_lowercase();
            n == "1" || n == "true" || n == "yes"
        })
        .unwrap_or(false);

    if full {
        return true;
    }

    is_shoppable(code)
}
